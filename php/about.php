    <div style="background-color:black;"><center><font color='white'>ABOUT</font></center></div>
    <p>Benue State University Project Supervision System is a computerized system designed to help in final project management where the system administrator who could be any IT professional employed by the school, can Add/Register Project Co-ordinators from various department of the school. This System is also designed to help Project Co-ordinator, Supervisors and Student in project monitoring by allowing Co-ordinators to add Supervisor and assign Students to him/her, Supervisor can view his/her assigned students, Approve/Rejects Project topic, Discus with a student and make project corrections. Using this system, a Student can submit project topic for approval, upload and download project for corrections as well as engage in discussion with his/her project Supervisor. </p>

<div style="background-color:black;"><center><font color='white'>AIMS AND OBJECTIVES</font></center></div><br>
The Aims objectives of this Project Supervision System are:<br>
i.	Develop a web application for communication between students and their project Supervisor.
<br>
ii.	Enable a Supervisor to select a student for discussion.
<br>
iii.	Enables both Project students and Supervisors Upload or Download project chapters for correction.

